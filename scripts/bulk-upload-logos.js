async function uploadLogos() {
  // Initialize Strapi app - exact same pattern as your working file
  const app = await strapi().load();
  
  try {
    console.log('Starting logo upload process...');
    
    // Get all domains from Strapi
    const domains = await strapi.entityService.findMany('api::domain.domain', {
      limit: -1 // Get all domains
    });
    
    console.log(`Found ${domains.length} domains in Strapi`);
    
    let successful = 0;
    let failed = 0;
    let skipped = 0;
    
    // Process each mapping
    for (const [imageFile, fullDomainName] of Object.entries(MANUAL_MAPPINGS)) {
      try {
        // Check if image exists
        const imagePath = path.join(LOGOS_FOLDER, imageFile);
        if (!fs.existsSync(imagePath)) {
          console.log(`Image not found: ${imageFile}`);
          skipped++;
          continue;
        }
        
        // Split domain name and extension
        const lastDotIndex = fullDomainName.lastIndexOf('.');
        if (lastDotIndex === -1) {
          console.log(`Invalid domain format: ${fullDomainName}`);
          skipped++;
          continue;
        }
        
        const domainName = fullDomainName.substring(0, lastDotIndex);
        const extension = fullDomainName.substring(lastDotIndex);
        
        // Find matching domain
        const domain = domains.find(d => 
          d.name.toLowerCase() === domainName.toLowerCase() &&
          d.extension.toLowerCase() === extension.toLowerCase()
        );
        
        if (!domain) {
          console.log(`Domain not found: ${domainName} ${extension}`);
          skipped++;
          continue;
        }
        
        if (domain.logo) {
          console.log(`Domain already has logo: ${fullDomainName}`);
          skipped++;
          continue;
        }
        
        // Upload the image
        const imageBuffer = fs.readFileSync(imagePath);
        const uploadedFiles = await strapi.plugins.upload.services.upload.upload({
          data: {},
          files: {
            files: {
              name: imageFile,
              type: 'image/png',
              size: imageBuffer.length,
              buffer: imageBuffer
            }
          }
        });
        
        if (uploadedFiles && uploadedFiles.length > 0) {
          // Update domain with logo
          await strapi.entityService.update('api::domain.domain', domain.id, {
            data: { logo: uploadedFiles[0].id }
          });
          
          console.log(`Successfully updated: ${fullDomainName} with ${imageFile}`);
          successful++;
        } else {
          console.log(`Failed to upload: ${imageFile}`);
          failed++;
        }
        
      } catch (error) {
        console.log(`Error processing ${imageFile}: ${error.message}`);
        failed++;
      }
    }
    
    console.log(`\nCompleted! Successful: ${successful}, Failed: ${failed}, Skipped: ${skipped}`);
    
  } catch (error) {
    console.error('Error:', error);
  } finally {
    // Properly close the app
    await app.destroy();
    process.exit(0);
  }
}

uploadLogos();